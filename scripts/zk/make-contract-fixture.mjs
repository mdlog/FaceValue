// FaceValue — generate a REAL BN254 Groth16 proof for the contract test fixture.
//
// Runs snarkjs.groth16.fullProve against the already-built artifacts
//   public/circuits/{resale.wasm, resale_final.zkey, verification_key.json}
// with the demo ticket from src/lib/zk/fixtures.json at a FAIR price, then
// converts the snarkjs DECIMAL coordinates into the EXACT BN254 byte layout the
// Soroban contract's bn254 from_bytes expects, and prints Rust array literals
// you can paste into contracts/facevalue/src/test.rs.
//
// BN254 serialization (from soroban-sdk 25.3.1 src/crypto/bn254.rs):
//   Bn254Fp       : 32-byte big-endian field element.
//   Bn254G1Affine : be32(X) || be32(Y)                              (64 bytes)
//   Bn254G2Affine : Fp2 = be32(c1) || be32(c0)  [imaginary FIRST];
//                   point = be32(X.c1)||be32(X.c0)||be32(Y.c1)||be32(Y.c0) (128 bytes)
//   Fr / signal   : 32-byte big-endian.
// snarkjs JSON stores G2 coords as [c0, c1] pairs, so each pair is SWAPPED here.

import { readFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";
import * as snarkjs from "snarkjs";
import { fpToBe32, g1ToBytes, g2ToBytes, u32Be, serializeVerificationKey } from "./groth16-bytes.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "../..");
const CIRCUITS = path.join(ROOT, "public", "circuits");

// ---- byte helpers (local only) ---------------------------------------------

function concatBytes(...arrs) {
  const total = arrs.reduce((n, a) => n + a.length, 0);
  const out = new Uint8Array(total);
  let off = 0;
  for (const a of arrs) { out.set(a, off); off += a.length; }
  return out;
}

/** Render a Uint8Array as a Rust `[u8; N]` array literal body (comma-separated). */
function rustBytes(arr) {
  const hex = [...arr].map((b) => "0x" + b.toString(16).padStart(2, "0"));
  const lines = [];
  for (let i = 0; i < hex.length; i += 12) {
    lines.push("    " + hex.slice(i, i + 12).join(", ") + ",");
  }
  return lines.join("\n");
}

// ---- main ------------------------------------------------------------------

async function main() {
  const fixtures = JSON.parse(
    await readFile(path.join(ROOT, "src", "lib", "zk", "fixtures.json"), "utf8"),
  );
  const vk = JSON.parse(await readFile(path.join(CIRCUITS, "verification_key.json"), "utf8"));

  if (vk.curve !== "bn128") throw new Error(`expected curve bn128, got ${vk.curve}`);
  if (vk.nPublic !== 3) throw new Error(`expected nPublic 3, got ${vk.nPublic}`);

  const t = fixtures.demoTicket;
  const evt = fixtures.events[t.eventId];
  const RESALE_PRICE = "11500"; // fair: <= perEventCap (12000)

  // Witness — public order is [perEventCap, merkleRoot, nullifierHash].
  const input = {
    resalePrice: RESALE_PRICE,
    ticketSecret: t.ticketSecret,
    pathElements: t.pathElements,
    pathIndices: t.pathIndices,
    perEventCap: evt.perEventCap,
    merkleRoot: evt.merkleRoot,
    nullifierHash: t.nullifierHash,
  };

  const { proof, publicSignals } = await snarkjs.groth16.fullProve(
    input,
    path.join(CIRCUITS, "resale.wasm"),
    path.join(CIRCUITS, "resale_final.zkey"),
  );

  // Sanity: snarkjs itself must verify before we trust the bytes.
  const ok = await snarkjs.groth16.verify(vk, publicSignals, proof);
  if (!ok) throw new Error("snarkjs.groth16.verify FAILED on freshly generated proof");

  // publicSignals order from circom `public [perEventCap, merkleRoot, nullifierHash]`.
  if (publicSignals.length !== 3) throw new Error(`expected 3 public signals`);
  const [capSig, rootSig, nullSig] = publicSignals;
  if (capSig !== evt.perEventCap) throw new Error(`cap signal mismatch: ${capSig}`);
  if (rootSig !== evt.merkleRoot) throw new Error(`root signal mismatch: ${rootSig}`);
  if (nullSig !== t.nullifierHash) throw new Error(`nullifier signal mismatch: ${nullSig}`);

  // ---- VK bytes (contract VerificationKey::to_bytes layout) ----
  // alpha(G1) | beta(G2) | gamma(G2) | delta(G2) | ic_len(u32 BE) | ic[*](G1)
  const vkBytes = serializeVerificationKey(vk);

  // ---- proof bytes (contract Proof::to_bytes layout): a(G1)|b(G2)|c(G1) ----
  const proofBytes = concatBytes(g1ToBytes(proof.pi_a), g2ToBytes(proof.pi_b), g1ToBytes(proof.pi_c));

  // ---- public-signals bytes (contract PublicSignals::to_bytes): len(u32 BE) | sig*32 ----
  const pubBytes = concatBytes(u32Be(publicSignals.length), ...publicSignals.map(fpToBe32));

  // ---- emit ----
  const out = [];
  out.push("// ===== AUTO-GENERATED by scripts/zk/make-contract-fixture.mjs =====");
  out.push(`// Real BN254 Groth16 proof for resale.circom, demo ticket @ price ${RESALE_PRICE}.`);
  out.push(`// publicSignals = [perEventCap=${capSig}, merkleRoot, nullifierHash]`);
  out.push("");
  out.push(`pub const REAL_CAP: i128 = ${capSig};`);
  out.push("");
  out.push(`// merkleRoot (decimal): ${rootSig}`);
  out.push(`pub const REAL_MERKLE_ROOT: [u8; 32] = [`);
  out.push(rustBytes(fpToBe32(rootSig)));
  out.push(`];`);
  out.push("");
  out.push(`// nullifierHash (decimal): ${nullSig}`);
  out.push(`pub const REAL_NULLIFIER: [u8; 32] = [`);
  out.push(rustBytes(fpToBe32(nullSig)));
  out.push(`];`);
  out.push("");
  out.push(`pub const REAL_VK_BYTES: [u8; ${vkBytes.length}] = [`);
  out.push(rustBytes(vkBytes));
  out.push(`];`);
  out.push("");
  out.push(`pub const REAL_PROOF_BYTES: [u8; ${proofBytes.length}] = [`);
  out.push(rustBytes(proofBytes));
  out.push(`];`);
  out.push("");
  out.push(`pub const REAL_PUBLIC_SIGNALS_BYTES: [u8; ${pubBytes.length}] = [`);
  out.push(rustBytes(pubBytes));
  out.push(`];`);
  out.push("");

  process.stdout.write(out.join("\n"));
  process.stderr.write(
    `\n[ok] snarkjs verified. vk=${vkBytes.length}B proof=${proofBytes.length}B pub=${pubBytes.length}B\n`,
  );
}

main().then(
  () => process.exit(0),
  (e) => {
    process.stderr.write(`[fail] ${e.stack || e}\n`);
    process.exit(1);
  },
);
